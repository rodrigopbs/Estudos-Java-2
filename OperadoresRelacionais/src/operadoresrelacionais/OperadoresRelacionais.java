package operadoresrelacionais;

/**
 *
 * @author Rodrigo
 */
public class OperadoresRelacionais {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        
        int valor1 = 1;
        int valor2 = 2;
        
        System.out.println("Valor 1 == valor 2: " + (valor1 == valor2));
        System.out.println("Valor 1 != valor 2: " + (valor1 != valor2));
        System.out.println("Valor 1 > valor 2: " + (valor1 > valor2));
        System.out.println("Valor 1 >= valor 2: " + (valor1 >= valor2));
        System.out.println("Valor 1 < valor 2: " + (valor1 < valor2));
        System.out.println("Valor 1 <= valor 2: " + (valor1 <= valor2));
        
    }
    
}
